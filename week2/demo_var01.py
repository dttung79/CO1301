# store what the user enter into variable name
# name is a variable of sting data type
name = input('What is your name? ')
print('Hello ', name)

# store what the user enter into variable age
# age is a variable of integer data type
age = int(input('How old are you? '))
print('You are ', age, ' years old')

# calculate the year of birth and store it into variable birth_year
birth_year = 2024 - age
print('You were born in ', birth_year)